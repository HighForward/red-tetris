

export function setColor(val: number)
{
  if (val === 1)
    return 'bg-purple'
  if (val === 2)
    return 'bg-yellow'
  if (val === 3)
    return 'bg-green'
  if (val === 4)
    return 'bg-blue'
  if (val === 5)
    return 'bg-orange'
  if (val === 6)
    return 'bg-bluesky'
  if (val === 7)
    return 'bg-red'
  if (val === 9)
    return 'bg-hardgray'
}
